<?php

namespace reporteBundle\Entity;

/**
 * encargado
 */
class encargado
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $nombres;

    /**
     * @var int
     */
    private $telefono;

    /**
     * @var string
     */
    private $eMail;

    /**
     * @var int
     */
    private $telExtencion;

    /**
     * @var string
     */
    private $turno;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nombres
     *
     * @param string $nombres
     *
     * @return encargado
     */
    public function setNombres($nombres)
    {
        $this->nombres = $nombres;

        return $this;
    }

    /**
     * Get nombres
     *
     * @return string
     */
    public function getNombres()
    {
        return $this->nombres;
    }

    /**
     * Set telefono
     *
     * @param integer $telefono
     *
     * @return encargado
     */
    public function setTelefono($telefono)
    {
        $this->telefono = $telefono;

        return $this;
    }

    /**
     * Get telefono
     *
     * @return int
     */
    public function getTelefono()
    {
        return $this->telefono;
    }

    /**
     * Set eMail
     *
     * @param string $eMail
     *
     * @return encargado
     */
    public function setEMail($eMail)
    {
        $this->eMail = $eMail;

        return $this;
    }

    /**
     * Get eMail
     *
     * @return string
     */
    public function getEMail()
    {
        return $this->eMail;
    }

    /**
     * Set telExtencion
     *
     * @param integer $telExtencion
     *
     * @return encargado
     */
    public function setTelExtencion($telExtencion)
    {
        $this->telExtencion = $telExtencion;

        return $this;
    }

    /**
     * Get telExtencion
     *
     * @return int
     */
    public function getTelExtencion()
    {
        return $this->telExtencion;
    }

    /**
     * Set turno
     *
     * @param string $turno
     *
     * @return encargado
     */
    public function setTurno($turno)
    {
        $this->turno = $turno;

        return $this;
    }

    /**
     * Get turno
     *
     * @return string
     */
    public function getTurno()
    {
        return $this->turno;
    }

            public function __toString()
    {
        return (string) $this->nombres;
    }
}
